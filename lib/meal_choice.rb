def meal_choice(broccoli, macaroni, protein="tofu")
  puts "A plate of meat with #{broccoli} and #{macaroni}."
  returns "What a nutritious meal!"
  puts "A plate of tofu with #{broccoli} and #{macaroni}."
  returns "What a nutritious meal!"
  puts "A plate of #{broccoli} and #{macaroni}."
  returns "What a nutritious meal!"
end
